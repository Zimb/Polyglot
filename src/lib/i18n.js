/**
 * Detect browser language, falling back to 'en' if not in supported list.
 */
export function detectBrowserLang() {
  if (typeof navigator === 'undefined') return 'en'
  const code = (navigator.language ?? '').split('-')[0].toLowerCase()
  return Object.prototype.hasOwnProperty.call(TRANSLATIONS, code) ? code : 'en'
}

/**
 * Returns a translation function for the given language code.
 * Usage: const t = getT('fr'); t('my_cards') → 'Mes fiches'
 *        t('cards_label', { n: 3 }) → '3 fiches'
 */
export function getT(lang) {
  const dict = TRANSLATIONS[lang] ?? TRANSLATIONS.en
  return function t(key, vars) {
    let str = dict[key] ?? TRANSLATIONS.en[key] ?? key
    if (vars) {
      Object.entries(vars).forEach(([k, v]) => {
        str = str.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v))
      })
    }
    return str
  }
}
